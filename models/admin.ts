import mongoose from "mongoose";

const adminSchema = new mongoose.Schema({
	username: {
		type: String,
		trim: true,
		required: true,
	},
	email: {
		type: String,
		trim: true,
		required: true,
	},
	password: {
		type: String,
		trim: true,
		required: true,
	},
});

export default mongoose.model("Admin", adminSchema);
